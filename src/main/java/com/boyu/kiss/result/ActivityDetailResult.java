package com.boyu.kiss.result;

public class ActivityDetailResult {
	
	private Integer SpecialOfferId;
	
	private String SpecialType;
	
	private String SpecialDetail;
	
	private String SpecialImgUrl;

	public Integer getSpecialOfferId() {
		return SpecialOfferId;
	}

	public void setSpecialOfferId(Integer specialOfferId) {
		SpecialOfferId = specialOfferId;
	}

	public String getSpecialType() {
		return SpecialType;
	}

	public void setSpecialType(String specialType) {
		SpecialType = specialType;
	}

	public String getSpecialDetail() {
		return SpecialDetail;
	}

	public void setSpecialDetail(String specialDetail) {
		SpecialDetail = specialDetail;
	}

	public String getSpecialImgUrl() {
		return SpecialImgUrl;
	}

	public void setSpecialImgUrl(String specialImgUrl) {
		SpecialImgUrl = specialImgUrl;
	}

}
