package com.boyu.kiss.entity;

import java.util.List;

public class Aaa {

	private int id;
	private String name;
	private String sex;
	private List<Yyy> yyy;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public List<Yyy> getYyy() {
		return yyy;
	}
	public void setYyy(List<Yyy> yyy) {
		this.yyy = yyy;
	}
	
	
}
